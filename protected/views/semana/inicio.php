<p>Este mes de mayo termina la <strong>Década Internacional de la Diversidad Biológica</strong>, una década dedicada a hacer esfuerzos para promover el conocimiento, valoración y buen manejo de la naturaleza. La mayoría de los países del mundo celebraron el <strong>22 de mayo, día internacional de la diversidad biológica</strong>.</p>

<p>En México, hemos celebrado nueve ediciones de la <strong>Semana de la Diversidad Biológica</strong> con una gran participación de la sociedad mexicana: escuelas, organizaciones de la sociedad civil, universidades, municipios, ejidos, comunidades, todos han participado promoviendo la naturaleza a través de charlas, conferencias, videos, salidas a playas, parques, arroyos, conciertos, manualidades, etc.</p>

<p>Para las Naciones Unidas, el 2020 es el <strong>Año Internacional de la Salud de las Plantas</strong>, un tema realmente tan importante como amplio, que va desde la conservación de los ecosistemas hasta nuestra alimentación. El Convenio de Diversidad Biológica celebra el Día Internacional de la Diversidad Biológica en el 2020 con el tema <strong>“Nuestras soluciones están en la naturaleza”</strong>.</p>

<p>Para participar en esta celebración, el Convenio de Diversidad Biológica sugiere los siguientes temas:</p>
<ul>
	<li>El entendimiento de la biodiversidad, su pérdida y soluciones para un futuro sostenible: la ciencia y el conocimiento tradicional</li>
	<li>Distintas maneras de conservar la biodiversidad</li>
	<li>Salud, agricultura, alimentación, seguridad alimentaria y biodiversidad</li>
	<li>Gente, cultura y biodiversidad, cambio climático, biodiversidad y tierra</li>
	<li>Nuestras soluciones están en la naturaleza</li>
</ul>

<p>Durante <strong>Mayo: mes de la naturaleza mexicana</strong>, como en los años anteriores, la Comisi&oacute;n Nacional para el Conocimiento y Uso de la  Biodiversidad <span class="conabio">(Conabio)</span> te invita a participar utilizando los matriales que hemos seleccionado y subiendo <a href="<?php echo Yii::app()->request->baseUrl; ?>/index.php?r=semana/admin">actividades en línea al calendario nacional de la página</a>.</p>

